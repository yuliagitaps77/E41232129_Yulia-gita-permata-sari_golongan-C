<?php
$x = 10;    // Deklarasi variabel $x dengan nilai 10
$y = 6;     // Deklarasi variabel $y dengan nilai 6

// Menampilkan hasil penjumlahan $x + $y (10 + 6 = 16)
echo ($x + $y);  
// Menampilkan hasil pengurangan $x - $y (10 - 6 = 4)
echo ($x - $y);  
// Menampilkan hasil perkalian $x * $y (10 * 6 = 60)
echo ($x * $y);  
// Menampilkan hasil pembagian $x / $y (10 / 6 = 1.6667)
echo ($x / $y);  
// Menampilkan sisa bagi $x % $y (10 % 6 = 4)
echo ($x % $y);  

$a = "Hello";  // Deklarasi variabel string $a dengan nilai "Hello"
$b = $a . " world!";  // Menggabungkan string $a dengan " world!" dan disimpan di $b
echo $b;  // Menampilkan nilai $b, yaitu "Hello world!"

// Menampilkan gabungan string dengan variabel $b
echo " -- " . $b . " ini string operator ";  
?>
